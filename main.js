/*
   De eerste interactie is al voor jullie uitgewerkt
   Als je op het 12e linkje klikt (“Interaction”), springt deze omhoog
*/

// Stap 1: selecteer het 12e linkje, en sla deze op in een variabele
let interaction = document.querySelector('a[href="#interaction"]')

// Stap 2: voeg de (click) event listener toe aan de link, met een callback functie
// In dit geval wordt de jumpHandler functie aangeroepen, zodra je op het linkje klikt
interaction.addEventListener('click', jumpHandler)

// Deze jumpHandler functie staat klaar voor als we 'm aanroepen; deze wordt dus
// _niet_ meteen bij het laden van de pagina aangeroepen
function jumpHandler() {

  // Stap 3: toggle een class op het element (zodra er dus op een link wordt geklikt)
  interaction.classList.toggle('jump')
}

// Extraatje, waardoor de class weer weggehaald wordt zodra de animatie afgelopen is
interaction.addEventListener('animationend', jumpHandler)



// ---------------------------------------------------------------------------------------------------------------Frontend interactie click -> scale

// Stap 1: querySelector
let frontend = document.querySelector('a[href="#frontend"]')

// Stap 2: addEventListener
frontend.addEventListener('click', scale)
function scale(){
  // Stap 3: (Callback functie met) classList (.toggle(), .add(), etc.)
  frontend.classList.toggle('scale')
}
frontend.addEventListener('animationend', scale)



